#include "../../inc/libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		finallen;
	char	*str;
	int		i;
	int		j;

	finallen = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = (char *)malloc(finallen * sizeof(char));
	if (str == NULL)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
		str[j++] = s1[i++];
	i = 0;
	while (s2[i])
		str[j++] = s2[i++];
	str[j] = '\0';
	return (str);
}
